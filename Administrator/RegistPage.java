    private void deleteSelectedRow() {
         DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Get the row's data
        String userID = tableModel.getValueAt(selectedRow, 0).toString();
        String role = tableModel.getValueAt(selectedRow, 1).toString();
        String userName = tableModel.getValueAt(selectedRow, 2).toString();
        String fullName = tableModel.getValueAt(selectedRow, 3).toString();
        String password = tableModel.getValueAt(selectedRow, 4).toString();
        String lineToDelete = userID + "," + role + "," + userName + "," + fullName + "," + password;

        // Remove the line from UserRef.txt
        List<String> lines = new ArrayList<>();
        try (BufferedReader bufferedReader = new BufferedReader(new FileReader("UserRef.txt"))) {
            String line;
            while ((line = bufferedReader.readLine()) != null) {
                if (!line.equals(lineToDelete)) {
                    lines.add(line);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (BufferedWriter bufferedWriter = new BufferedWriter(new FileWriter("UserRef.txt"))) {
            for (String line : lines) {
                bufferedWriter.write(line);
                bufferedWriter.newLine();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error writing to file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Remove the row from the table
        tableModel.removeRow(selectedRow);

        JOptionPane.showMessageDialog(this, "Selected row deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }
    
     private void populateTable() {
       DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        try (BufferedReader bufferedReader = new BufferedReader(new FileReader("UserRef.txt"))) {
            String line;
            while ((line = bufferedReader.readLine()) != null) {
                String[] col = line.split(",");
                if (col.length == 5) { // Ensure correct number of columns
                    tableModel.addRow(col);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading from file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
