    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String UserID = UserIDText.getText(); 
        String Role = RoleSelect.getSelectedItem().toString(); 
        String UserName = UsernameText.getText(); 
        String FullName = FullNameText.getText(); 
        String Password = PasswordText.getText();

        try { 
            // Check if the user ID already exists in the file
            boolean isDuplicateUserID = false;
            boolean isDuplicateUserName = false;

            List<String> lines = new ArrayList<>();
            Path filePath = Paths.get("UserRef.txt");
            try (BufferedReader reader = new BufferedReader(new FileReader(filePath.toFile()))) {
                String line;
                while ((line = reader.readLine()) != null) {
                    String[] parts = line.split(",");
                    if (parts.length >= 1 && parts[0].equals(UserID)) {
                        isDuplicateUserID = true;
                        break;
                    }
                    if (parts.length >= 1 && parts[2].equals(UserName)) {
                        isDuplicateUserName = true;
                        break;
                    }
                    lines.add(line);
                }
            }

            if (isDuplicateUserID) {
                JOptionPane.showMessageDialog(this, 
                    "Error: User ID already exists in the file.", 
                    "Duplicate User ID", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
             if (isDuplicateUserName) {
                JOptionPane.showMessageDialog(this, 
                    "Error: User Name already exists in the file.", 
                    "Duplicate User Name", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Write the new user information to the file
            FileWriter fileWriter = new FileWriter("UserRef.txt", true);
            BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);
            bufferedWriter.write(UserID + "," + Role + "," + UserName + "," + FullName + "," + Password);
            bufferedWriter.newLine(); // Add a new line after each entry
            bufferedWriter.close();
            fileWriter.close();

            System.out.println("User information saved successfully to UserRef.txt");
            JOptionPane.showMessageDialog(this, "User information saved successfully!");

            // Clear input fields
            UserIDText.setText("");
            RoleSelect.setSelectedIndex(0);
            UsernameText.setText("");
            FullNameText.setText("");
            PasswordText.setText("");

            // Refresh the table
            populateTable();
        } catch (IOException e) {
            System.err.println("Error writing to file: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

